def predict_competency_level(score, course=None, other_scores=None):
    # (оставляем без изменений)
    if score < 400:
        level = "начальный"
        level_code = 0
    elif score < 600:
        level = "средний"
        level_code = 1
    else:
        level = "высокий"
        level_code = 2
    percentile = ((score - 200) / 600) * 100
    return {
        "level": level,
        "level_code": level_code,
        "confidence": 1.0,
        "percentile": int(max(0, min(100, percentile))),
        "probabilities": {
            "начальный": 1.0 if level_code == 0 else 0.0,
            "средний": 1.0 if level_code == 1 else 0.0,
            "высокий": 1.0 if level_code == 2 else 0.0
        }
    }


